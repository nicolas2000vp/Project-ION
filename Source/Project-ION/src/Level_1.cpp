int Level_1::gameLoop(){

    printMap();
    if (key[KEY_C])
    {
        jack->jump();
    }
    if (key[KEY_X] && !(key[KEY_DOWN]))
    {
        jack->JetPack();
    }
    if (key[KEY_X] && key[KEY_DOWN])
    {
        jack->activeDash();
    }

    if (key[KEY_LEFT])
    {
        jack->move(-1);
    }
    if (key[KEY_RIGHT])
    {
        jack->move(1);
    }

    jack->loop();
    jack->print(buffer);
}

