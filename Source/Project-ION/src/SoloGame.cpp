void SoloGame::execute()
{
    int i, j, pos;
    while(!key[KEY_ESC])
    {
        current->printMap();
        if (key[KEY_UP])
        {
            jack->move(0, -1);
        }
        if (key[KEY_DOWN])
        {
            jack->move(0, 1);
        }
        if (key[KEY_LEFT])
        {
            jack->move(-1, 0);
        }
        if (key[KEY_RIGHT])
        {
            jack->move(1, 0);
        }
        current->updatePosition();
        jack->print(buffer);
        jack->getPosMatrix(&i, &j);
        pos = current->getValueMap(i, j);

        cout << jack->isStructureCollide() << endl;

        if(pos == 5){
            cout << "Next level" << endl;
            break;
        }

        draw_sprite(screen, buffer, 0, 0);
        clear_bitmap(buffer);
    }
}

