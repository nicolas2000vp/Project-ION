void SoloGame::execute()
{
    //int i, j, pos;
    while(!key[KEY_ESC])
    {
        current->printMap();
        if (key[KEY_C])
        {
            jack->jump();
        }
        if (key[KEY_X])
        {
            jack->JetPack();
        }
       /* if (key[KEY_DOWN])
        {
            jack->move(0, 1);
        }
        */
        if (key[KEY_LEFT])
        {
            jack->move(-1);
        }
        if (key[KEY_RIGHT])
        {
            jack->move(1);
        }

        current->updatePosition();
        top->print(buffer);
        jack->print(buffer);
        //jack->getPosMatrix(&i, &j);
        //pos = current->getValueMap(i, j);
        top->move(0);
        jack->gravity();
        jack->isStructureCollide();

        draw_sprite(screen, buffer, 0, 0);
        clear_bitmap(buffer);
    }
}

