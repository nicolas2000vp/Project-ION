void SoloGame::execute()
{
    while(!key[KEY_ESC])
    {
        current->printMap();
        if (key[KEY_C])
        {
            jack->jump();
        }
        if (key[KEY_X] && !(key[KEY_DOWN]))
        {
            jack->JetPack();
        }
        if (key[KEY_X] && key[KEY_DOWN])
        {
            jack->activeDash();
        }

        if (key[KEY_LEFT])
        {
            jack->move(-1);
        }
        if (key[KEY_RIGHT])
        {
            jack->move(1);
        }

        jack->loop();
        top->loop();
        //current->updatePosition();
        top->print(buffer);
        jack->print(buffer);

//        if(jack->isCollide(static_cast<Entity*>(top))){
//            cout << "ai ai ai " << endl;
//        }
//        else{
//            //cout << "nÃ£o" << endl;
//        }

        draw_sprite(screen, buffer, 0, 0);
        clear_bitmap(buffer);
    }
}

