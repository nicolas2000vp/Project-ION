int Level_2::gameLoop()
{

    game_status = genericGameLoop();
    if(game_status != 1)
        return game_status;

    if(jack->getx() >= 1075 && jack->gety() >= 0){
        resetPlayer(-15,jack->gety());
        return 2; //next level
    }

    if(jack->getx() <= -20){
        resetPlayer(1050,jack->gety());
        return 3; //prev level
    }

    jack->print(buffer);
}

