int Tutorial::gameLoop(){
    printMap();
    if (key[KEY_C])
    {
        jack->jump();
    }
    if (key[KEY_X] && !(key[KEY_DOWN]))
    {
        jack->JetPack();
    }
    if (key[KEY_X] && key[KEY_DOWN])
    {
        jack->activeDash();
    }

    if (key[KEY_LEFT])
    {
        jack->move(-1);
    }
    if (key[KEY_RIGHT])
    {
        jack->move(1);
    }

    jack->loop();
    isStructureCollide(static_cast<Character*>(jack));
    loopCharacters();
    collisionCharacters();
    updatePosition();
    jack->print(buffer);

    if(isCollide(static_cast<Entity*>(jack), static_cast<Entity*>((*characters)[0]))){
        return 0;
    }
    if(isCollide(static_cast<Entity*>(jack), static_cast<Entity*>((*characters)[1]))){
        return 0;
    }

    if(jack->getx() >= 1050 && jack->gety() >  510){
        return 2; //next level
    }
    return 1;
}

