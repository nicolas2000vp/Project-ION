int Tutorial::gameLoop(){

    game_status = genericGameLoop();

    if(game_status != 1)
        return game_status;

    if(jack->getx() >= 1075){
        resetPlayer(-15,jack->gety());
        return 2; //next level
    }

    return 1;
}

